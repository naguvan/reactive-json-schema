export type IMatcher = (value: string) => boolean;

export interface IMatchers {
  [key: string]: IMatcher;
}

// export type IFormat = keyof typeof formats | 'uppercase' | 'lowercase' | 'utc-millisec';

export const matchers: IMatchers = Object.keys(formats).reduce(
  // tslint:disable-next-line:no-shadowed-variable
  (matchers: IMatchers, format: string) => {
    matchers[format] = (value: string) =>
      !!value.match((formats as any)[format]);
    return matchers;
  },
  {
    lowercase: (value: string) => value === value.toLowerCase(),
    regex: (value: string) => {
      try {
        return !!new RegExp(value);
      } catch (e) {
        return false;
      }
    },
    uppercase: (value: string) => value === value.toUpperCase(),
    "utc-millisec": (value: string) =>
      typeof value === "string" &&
      parseFloat(value) === parseInt(value, 10) &&
      !Number.isNaN(Number(value))
  }
);
